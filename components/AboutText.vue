import { gsap } from 'gsap'
export default {
	mounted() {
		this.animate()
		this.eyeAnimation()
	},
	methods: {
		animate() {
			const animationContainer = this.$el.querySelector('.js-animate')
			const elements = this.$el.querySelectorAll('.js-animate > *')

			const tl = gsap.timeline({
				scrollTrigger: {
					trigger: animationContainer,
					start: 'top 80%',
				},
			})

			tl.from(elements, {
				autoAlpha: 0,
				x: '50%',
			}).to(elements, {
				x: 0,
				stagger: {
					each: 0.1,
					ease: 'power2',
				},
				autoAlpha: 1,
			})
		},
		eyeAnimation() {
			const lidLeft = this.$refs.lidLeft
			const lidRight = this.$refs.lidRight

			const tl = gsap.timeline()

			gsap.set([lidLeft, lidRight], {
				translateY: '-100%',
			})

			tl.to([lidLeft, lidRight], {
				translateY: 0,
				duration: 0.2,
			}).to([lidLeft, lidRight], {
				translateY: '-100%',
				duration: 0.2,
			})

			tl.repeat(-1).repeatDelay(7)
		},
	},
}
