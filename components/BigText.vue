import { gsap } from 'gsap'
export default {
	mounted() {
		this.animateText()
	},
	methods: {
		animateText() {
			const text = this.$el.querySelectorAll('main p')
			const stickOne = this.$el.querySelector('#stick-1')
			const stickTwo = this.$el.querySelector('#stick-2')
			const allBars = this.$el.querySelectorAll('.bars')
			const tl = gsap.timeline()

			tl.from(stickOne, {
				x: '-100%',
			})
				.from(
					stickTwo,
					{
						x: '100%',
					},
					0
				)
				.fromTo(
					allBars,
					{
						autoAlpha: 0,
					},
					{
						autoAlpha: 1,
						stagger: 0.02,
					}
				)

			gsap.fromTo(
				text,
				{
					x: '50%',
					autoAlpha: 0,
				},
				{
					x: 0,
					autoAlpha: 1,
				}
			)
		},
	},
}
